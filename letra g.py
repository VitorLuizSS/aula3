#Combinações de 4 valores

a = int(input("A: "))
b = int(input("B: "))
c = int(input("C: "))
d = int(input("D: "))

print("Adições:")
print("A+B =", a+b)
print("A+C =", a+c)
print("A+D =", a+d)
print("B+C =", b+c)
print("B+D =", b+d)
print("C+D =", c+d)

print("\nMultiplicações:")
print("A*B =", a*b)
print("A*C =", a*c)
print("A*D =", a*d)
print("B*C =", b*c)
print("B*D =", b*d)
print("C*D =", c*d)
